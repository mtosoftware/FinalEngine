namespace FinalEngine.Rendering.Buffers
{
    /// <summary>
    ///   Enumerates the available buffer types.
    /// </summary>
    public enum BufferType
    {
        /// <summary>
        ///   Specifies the buffer stores vertex data.
        /// </summary>
        VertexBuffer,

